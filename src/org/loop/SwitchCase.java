package org.loop;

public class SwitchCase {
	public static void main(String[] args) {
		int i=1;
		switch(i)
		{
		case 0:System.out.println("I=0");
		break;
		case 1: System.out.println("I=1");
		break;
		case 2: System.out.println("I=2");
		break;
		case 3: System.out.println("I=3");
		break;
		default:
			System.out.println("none");
		break;
		}
	}
}
